package ca.hexanome04.splendorgame.model.gameversions;

/**
 * Constants for the different playable expansion combinations.
 */
public enum GameVersions {

    /**
     * Base game + Orient expansion.
     */
    BASE_ORIENT,

    /**
     * Base game + Orient and Cities expansions.
     */
    BASE_ORIENT_CITIES,

    /**
     * Base game + Orient and Trade Routes expansions.
     */
    BASE_ORIENT_TRADE_ROUTES,

}
